def get_verse(book, chapter, verse, language="pt"):
    """Get a specific verse in the specified language."""
    try:
        return BIBLE_DATA[book][str(chapter)][str(verse)][language]
    except KeyError:
        return None


def get_chapter(book, chapter, language="pt"):
    """Get all verses from a chapter in the specified language."""
    try:
        chapter_data = BIBLE_DATA[book][str(chapter)]
        verses = {}
        for verse_num, verse_data in chapter_data.items():
            if language in verse_data:
                verses[verse_num] = verse_data[language]
        return verses
    except KeyError:
        return None


